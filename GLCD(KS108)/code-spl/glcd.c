//-------------------------------------------------------------------------------------------------
void GLCD_EnableController(unsigned char controller)
{
    switch(controller)
    {
        case 0 : GPIO_ResetBits(GLCD_CS2_PORT, GLCD_CS2_PIN); break;
        case 1 : GPIO_ResetBits(GLCD_CS1_PORT, GLCD_CS1_PIN); break;
    }
}
