void GLCD_DisableController(unsigned char controller)
{
switch(controller){
	case 0 : GPIO_SetBits(GLCD_CS2_PORT, GLCD_CS2_PIN); break;
	case 1 : GPIO_SetBits(GLCD_CS1_PORT, GLCD_CS1_PIN); break;
	}
}

