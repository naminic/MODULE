void putpixel(unsigned int x, unsigned int y)
{
    unsigned char tmp=0;
	  tmp=DisplayData[x][ y / 8 ];
    GLCD_GoTo(x, (y / 8));
    tmp |= (1 << (y % 8));
    GLCD_WriteData(tmp);
}

